package Week15Algo

/**
 * Candidate Instructions
Task
An isogram is a word that has no repeating letters, consecutive or non-consecutive.

Implement a function that determines whether a string is an isogram. Assume an empty string is an isogram, and ignore letter case.

Examples
Input	Output	Comments
"Dermatoglyphics"	true
"aba"	false
"moOse"	false	Ignore letter case.
Documentation
isIsogram(str)
Parameters:
str: String
A string of characters.

Guaranteed Constraints:
The string will never be null or undefined.
The string may be empty (""), and may contain any alphabetic character.
Returns: boolean
Return true if the string contains only unique characters (ignoring case), otherwise return false.
 */


fun isIsogram(str: String): Boolean {
    if(str == "") return true
    val strToLower = str.lowercase()
    for (charac in strToLower.indices)
        if (charac != str.lastIndexOf(strToLower[charac])) return false
    return true
}
